fun add(n1: Int, n2: Int) {
    println(n1+n2)
}

fun subtract(n1: Int, n2: Int) {
    println(n1-n2)
}

fun multiply(n1: Int, n2: Int) {
    println(n1*n2)
}

fun divide(n1: Int, n2: Int) {
    println(n1/n2)
}

fun main() {
	
    val x = 3
    val y = 4
    
    add(x,y)
    subtract(x,y)
    multiply(x,y)
    divide(x,y)
    
}
